package basicJava;

public class TempSwap {

	public static void main(String[] args) {
				
			int x=61;
			
			boolean prime=true;
			
			for(int i=2;i<x;i++) {
				
				if(x%i==0) {
					
					prime=false;
					
					break;
					
					
					
					
				}
				
				
				
			}
			if (prime==true)
				System.out.println(x+ " is a prime number");
			
			else
				System.out.println(x + " is not a prime number");
				
				
				
				
				
				
				
			}
			
			
		
		
		
				
				
		
		
		
		
		
		

	

}
